package battleshipwarfare.Elementspackage;

/**
 * Enumerator that represents the posiible element type.
 * @author nuno.sousa
 */
@SuppressWarnings("serial")
public enum ElementType {

    /**
     * A water element.
     */
    WATER,
    /**
     * A one point element.
     */
    SUBMARINE,
    /**
     * A two point element.
     */
    PATROL_BOAT,
    /**
     * A three point element.
     */
    DESTROYER,
    /**
     * A four point element.
     */
    BATTLESHIP,
    /**
     * A five point element.
     */
    AIRCRAFT
}
